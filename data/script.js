    // Loglarƒ± API'den √ßek
    async function fetchLogs() {
        if (state.logPaused) {
            console.log('Log yenileme duraklatƒ±ldƒ±');
            return;
        }
        
        try {
            const response = await secureFetch('/api/logs');
            if (response && response.ok) {
                const logs = await response.json();
                if (Array.isArray(logs)) {
                    allLogs = logs;
                    
                    // Kaynak listesini g√ºncelle
                    updateSourceFilter();
                    
                    // Filtreleri uygula
                    applyFilters();
                    
                    console.log(`‚úÖ ${logs.length} log y√ºklendi`);
                } else {
                    console.error('Ge√ßersiz log formatƒ±:', logs);
                }
            }
        } catch (error) {
            console.error('Log y√ºkleme hatasƒ±:', error);
            if (!logContainer.innerHTML.includes('error')) {
                showMessage('Log kayƒ±tlarƒ± y√ºklenemedi', 'error');
            }
        }
    }

    // Kaynak filtresini dinamik olarak g√ºncelle
    function updateSourceFilter() {
        if (!logSourceFilter) return;
        
        const sources = new Set(['all']);
        allLogs.forEach(log => sources.add(log.s));
        
        const currentValue = logSourceFilter.value;
        
        // Mevcut se√ßenekleri temizle (all hari√ß)
        while (logSourceFilter.children.length > 1) {
            logSourceFilter.removeChild(logSourceFilter.lastChild);
        }
        
        // Yeni se√ßenekler ekle
        Array.from(sources).sort().forEach(source => {
            if (source !== 'all') {
                const option = document.createElement('option');
                option.value = source;
                option.textContent = source;
                logSourceFilter.appendChild(option);
            }
        });
        
        // Eski deƒüeri geri y√ºkle
        if (sources.has(currentValue)) {
            logSourceFilter.value = currentValue;
        }
    }

